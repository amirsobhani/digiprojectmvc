    $('.product-item').hover(function(){
        $(this).find('.item-compare').fadeIn(100);
        $(this).find('.product-title span').css({whiteSpace : "normal"});
    },function(){
        if(($(this).find('.compare-check').is(':checked') == false)){
            $(this).find('.item-compare').fadeOut(100);
        }
        $(this).find('.product-title span').css({whiteSpace : "nowrap"});
    });

    $('.compare-check').click(function(){
        var parent = $(this).parent();
        var parents = $(this).parents('.item-compare');
        if($(this).is(':checked')){
            parent.find('.activeCompare').addClass('activeCompareChecked');
            parent.css({backgroundColor:"#ECF7FF"});
            parents.css({display:"block"});
        }
        else{
            parent.find('.activeCompare').removeClass('activeCompareChecked');
            parent.css({backgroundColor:"#f5f6f7"});
        }
    });


